function loadSubjects(key, isSenior = false, stdName = "") {
    updateQuote();
// આ લાઈન્સ ઉમેરો



    let list = null;

    if (isSenior) {
        // તમારા વીડિયોમાં 'Streams' છે, એટલે આપણે એ જ રાખીશું
        let stdData = data[stdName]; 
        
        if (stdData) {
            let streamsData = stdData["Streams"]; 
            if (streamsData) {
                list = streamsData[key];
            }
        }
    } else {
        list = data[key];
    }

    if (!list) {
        contentArea.innerHTML = `
            <button class="back-btn" onclick="${isSenior ? `loadStreamSelection('${stdName}')` : 'loadHome()'}">← પાછા</button>
            <h3 style="text-align:center; margin-top:20px;">આ પ્રવાહનો ડેટા હજુ ઉમેર્યો નથી.</h3>
        `;
        return;
    }

    contentArea.innerHTML = `<button class="back-btn" onclick="${isSenior ? `loadStreamSelection('${stdName}')` : 'loadHome()'}">← પાછા</button>`;
    
    Object.keys(list).forEach((sub, index) => {
        let btn = document.createElement('button');
        btn.className = `btn-card grad-${(index % 5) + 1}`;
        btn.innerText = sub;
        btn.onclick = () => loadChapters(list[sub], sub, key, isSenior, stdName);
        contentArea.appendChild(btn);
    });
}
function loadChapters(chapters, subName, backKey, isSenior, stdName) {
    updateQuote();
    
    contentArea.innerHTML = `
        <button class="back-btn" onclick="loadSubjects('${backKey}', ${isSenior}, '${stdName}')">← પાછા</button>
        <h3>${subName}</h3>
    `;

    if (!chapters) {
        contentArea.innerHTML += `<h3 style="text-align:center; margin-top:20px;">આ વિષયના પ્રકરણો હજુ ઉમેર્યા નથી.</h3>`;
        return;
    }

    chapters.forEach((chap, index) => {
        let div = document.createElement('div');
        div.className = `btn-card grad-${(index % 5) + 1}`;
        // મેં અહીં તમારા કહેવા મુજબ સ્ટાર (⭐) આઇકન જ રાખ્યું છે
        div.innerHTML = `${chap} <span style="float: right; margin-left: 10px;" class="pin-btn ${pinnedQuestions.includes(chap) ? 'active' : ''}" onclick="event.stopPropagation(); togglePin('${chap}')">${pinnedQuestions.includes(chap) ? '⭐' : '☆'}</span>`;


        div.onclick = () => loadOptionsPage(chap, subName, backKey, isSenior, stdName);
        contentArea.appendChild(div);
    });
}



